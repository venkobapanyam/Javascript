//Switch-Case (In switch case it behaves like thrible equal to, it is checking data type also.)
let grade='A';
switch(grade)
{
    case 'A':console.log('Excellent');
    break;
    case 'B':console.log('Good');
    break;
    case 'C':console.log('Ok');
    break;
    case 'D':console.log('Bad');
    break;
    default:console.log('unknown grade');
}